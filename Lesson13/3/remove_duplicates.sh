#!/bin/bash

# Удаляет повторяющиеся слова, разделенные пробельными символами

if [ $# -eq 0 ]; then
    # Если аргументов нет, читаем из stdin
    input_text=$(cat)
else
    # Если указан файл, читаем из файла
    input_text=$(cat "$1")
fi

# Обрабатываем текст с помощью sed
echo "$input_text" | sed -E ':a; s/([[:alpha:]]+)[[:space:]]+\1/\1/g; ta'