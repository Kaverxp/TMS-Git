#!/bin/bash

# Имя файла для записи
output_file="arguments_output.txt"

# Вывод в консоль
echo "Все аргументы командной строки:"
echo "Количество аргументов: $#"
echo "Аргументы: $@"

echo "----------------------------------------"

# Вывод каждого аргумента по отдельности
echo "Подробный вывод:"
for arg in "$@"; do
    echo "Аргумент: $arg"
done

# Запись в файл
{
    echo "=== Запись от $(date) ==="
    echo "Все аргументы командной строки:"
    echo "Количество аргументов: $#"
    echo "Аргументы: $@"
    echo "----------------------------------------"
    echo "Подробный вывод:"
    for arg in "$@"; do
        echo "Аргумент: $arg"
    done
} > "$output_file"

echo "----------------------------------------"
echo "Результат также записан в файл: $output_file"
